#!/usr/bin/env python3
"""
    Module has function that returns sum of two floats
"""


def concat(str1: str, str2: str) -> str:
    """
        returns the sum of a and b

        Args:
            str1 (str): string
            str2 (str): string

        Returns:
            concat of str1 and str2
    """
    return str1 + str2
